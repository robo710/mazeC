#pragma region 맵 그리기
void PrintMaze(char mazeboard[MAZESIZE][MAZESIZE], int color)
{
	for (int i = 0; i < MAZESIZE; i++)
	{
		for (int j = 0; j < MAZESIZE; j++)
		{
			if (mazeboard[i][j] == PLAYER || mazeboard[i][j] == '.')
			{
				if (mazeboard[i - 1][j] == PLAYER)
				{
					mazeboard[i][j] = '@';
				}
				else if (mazeboard[i][j - 1] == PLAYER)
				{
					mazeboard[i][j] = '@';
				}
				else if (mazeboard[i + 1][j] == PLAYER)
				{
					mazeboard[i][j] = '@';
				}
				else if (mazeboard[i][j + 1] == PLAYER)
				{
					mazeboard[i][j] = '@';
				}
			}
		}
	}
	// 맵 그리기
	for (int i = 0; i < MAZESIZE; i++)
	{
		for (int j = 0; j < MAZESIZE; j++)
		{
			if (mazeboard[i][j] == PLAYER)
			{
				fillcolorset(LIGHTYELLOW);
				printf("%c ", mazeboard[i][j]);
			}
			else if (mazeboard[i][j] == GOAL || mazeboard[i][j] == '.')
			{
				fillcolorset(RED);
				printf("%c ", mazeboard[i][j]);
			}
			else if (mazeboard[i][j] == '@')
			{
				if (color == 1)
					fillcolorset(AQUA);
				else if (color == 2)
					fillcolorset(LIGHTAQUA);
				else if (color == 3)
					fillcolorset(LIGHTBLUE);
				else if (color == 4)
					fillcolorset(GRAY);
				else if (color == 5)
					fillcolorset(GREEN);
				printf("%c ", mazeboard[i][j]);
			}
			else
			{
				fillcolorset(BLACK);
				printf("%c ", mazeboard[i][j]);
			}
		}
		printf("\n");
	}
	fillcolorset(BLACK);
	gotoxy(0, 0);
}
