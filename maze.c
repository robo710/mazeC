#pragma region 캐릭터 이동
void Move(char mazeboard[MAZESIZE][MAZESIZE], int* x, int* y, int map)
{
	// 캐릭터 이동
	char go = _getch();
	if (go == 'w' || go == 'W' || go == UP)
	{
		if (mazeboard[*x - 1][*y] == ' ' || mazeboard[*x - 1][*y] == GOAL)
		{
			*x = *x - 1;
			mazeboard[*x + 1][*y] = ' ';
			mazeboard[*x][*y] = PLAYER;
		}
	}
	else if (go == 'a' || go == 'A' || go == LEFT)
	{
		if (mazeboard[*x][*y - 1] == ' ' || mazeboard[*x][*y - 1] == GOAL)
		{
			*y = *y - 1;
			mazeboard[*x][*y + 1] = ' ';
			mazeboard[*x][*y] = PLAYER;
		}
	}
	else if (go == 's' || go == 'S' || go == DOWN)
	{
		if (mazeboard[*x + 1][*y] == ' ' || mazeboard[*x + 1][*y] == GOAL)
		{
			*x = *x + 1;
			mazeboard[*x - 1][*y] = ' ';
			mazeboard[*x][*y] = PLAYER;
		}
	}
	else if (go == 'd' || go == 'D' || go == RIGHT)
	{
		if (mazeboard[*x][*y + 1] == ' ' || mazeboard[*x][*y + 1] == GOAL)
		{
			*y = *y + 1;
			mazeboard[*x][*y - 1] = ' ';
			mazeboard[*x][*y] = PLAYER;
		}
	}
	else if (go == ESC)
	{
		system("cls");
		switch (map)
		{
		case 1:
			easystage();
			break;
		case 2:
			normalstage();
			break;
		case 3:
			hardstage();
			break;
		}
	}
}
