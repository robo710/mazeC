int main()
{
	static int re = 0;
	if (re == 0)
	{
		PlaySound(TEXT(MAINBGM), NULL, SND_FILENAME | SND_ASYNC | SND_LOOP);
		system("mode con cols=120 lines=30");
		RemoveCursor();
	}
	mainTitleDraw(true);
	while (1) {
		re = 1;
		char start;
		start = _getch();
		system("cls");
		if (start == '\r') {
			difficulty();
		}
		else if (start == 'Q' || start == 'q')
		{
			explanation();
		}
		else if (start == ESC)	
		{
			while (1)
			{
				Box();
				setColor(BRIGHTWHITE);
				gotoxy(47, 10);
				printf("게임을 종료하시겠습니까?");
				Y();
				Or();
				N();

				start = _getch();
				if (start == 'Y' || start == 'y')
				{
					system("cls");
					printf("게임을 종료하였습니다.");
					exit(1);
				}
				else if (start == 'N' || start == 'n')
				{
					system("cls");
					mainTitleDraw(true);
					break;
				}
			}
		}
		else
		{
			subTitleDraw(true);
		}
	}
}
